// Declara un array con tres nombres y usa un bucle for para imprimir cada nombre en la consola.
let nombresPersonas = ['Alba', 'Pepa', 'Lourdes'];

// Usar un bucle for para imprimir cada nombre
for (let i = 0; i < nombresPersonas.length; i++) {
    console.log(nombresPersonas[i]);
}